/* Reading Stored Values

  you can also read the value stored in a variable
  to read a variable's value you just write it somewhere!
    somewhere other than the left side of =

  you can use variables every way as many times as you like

*/

let name = 'Peter';

let greeting = 'hello ' + name;
console.log(greeting); // "hello Pieter"

name = 'Amelia';

greeting = 'good bye ' + name;
console.log(greeting); // "good bye Amela"
